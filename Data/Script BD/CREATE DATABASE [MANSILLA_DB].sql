CREATE PROCEDURE [dbo].[SP_validateUser]
	@username varchar(50), @password varchar(50)
AS
BEGIN
	SET NOCOUNT ON;
	SELECT 'true' UserExist
	WHERE EXISTS (select * from USUARIOS u where u.userName like @username and u.userPassword like @password)
END
